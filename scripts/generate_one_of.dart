/// Generates packages/degenerate_runtime/lib/src/one_of.dart
///
/// Run: dart run scripts/generate_one_of.dart
import 'dart:io';

const maxArity = 9;
const letters = ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I'];

void main() {
  final buf = StringBuffer();

  buf.writeln('/// Typed union containers for OpenAPI `oneOf` / `anyOf` schemas.');
  buf.writeln('///');
  buf.writeln('/// Instead of generating a sealed class per union, specs emit a typedef:');
  buf.writeln('/// ```dart');
  buf.writeln('/// typedef FilterValue = OneOf3<String, double, bool>;');
  buf.writeln('/// ```');
  buf.writeln('///');
  buf.writeln('/// Construction: `OneOf3.a(\'hello\')` or `OneOf3.from(\'hello\')`');
  buf.writeln('/// Reading: `switch (v.value) { case String s: ... }`');
  buf.writeln('library;');
  buf.writeln();
  buf.writeln("// GENERATED by scripts/generate_one_of.dart — do not edit by hand.");
  buf.writeln();
  buf.writeln("import 'package:meta/meta.dart';");
  buf.writeln();
  buf.writeln('String _oneOfError(Object? json, List<(String, Object)> errors) {');
  buf.writeln("  final variants = errors.map((e) => '  \${e.\$1}: \${e.\$2}').join('\\n');");
  buf.writeln("  return 'No variant matched for value (\$json):\\n\$variants';");
  buf.writeln('}');

  for (var n = 2; n <= maxArity; n++) {
    _writeOneOf(buf, n);
  }

  final outPath = 'packages/degenerate_runtime/lib/src/one_of.dart';
  File(outPath).writeAsStringSync(buf.toString());
  print('Wrote $outPath (${buf.length} chars)');
}

void _writeOneOf(StringBuffer buf, int n) {
  final typeParams = letters.sublist(0, n).join(', ');
  final cls = 'OneOf$n<$typeParams>';

  buf.writeln();
  buf.writeln('// ─── OneOf$n ${'─' * (60 - 'OneOf$n'.length)}');
  buf.writeln();
  buf.writeln('/// Typed union of $n variants.');
  buf.writeln('@immutable');
  buf.writeln('final class $cls {');

  // Private const constructor
  buf.writeln('  const OneOf$n._(this.value);');
  buf.writeln();

  // Named variant constructors
  for (var i = 0; i < n; i++) {
    final letter = letters[i].toLowerCase();
    final type = letters[i];
    buf.writeln('  /// Wraps a value of variant $type.');
    buf.writeln('  const OneOf$n.$letter($type value) : this._(value);');
  }
  buf.writeln();

  // .from() factory
  buf.writeln('  /// Wraps a typed value, matching by runtime type.');
  buf.writeln('  factory OneOf$n.from(Object? value) {');
  for (var i = 0; i < n; i++) {
    buf.writeln('    if (value is ${letters[i]}) return OneOf$n._(value);');
  }
  final throwMsg = "Value \$value is not ${letters.sublist(0, n).join(' or ')}";
  final throwLine = "    throw ArgumentError('$throwMsg');";
  if (throwLine.length > 80) {
    buf.writeln('    throw ArgumentError(');
    buf.writeln("      '$throwMsg',");
    buf.writeln('    );');
  } else {
    buf.writeln(throwLine);
  }
  buf.writeln('  }');
  buf.writeln();

  // parse() static method
  final parseTypeParams = letters.sublist(0, n).join(', ');
  buf.writeln('  /// Deserializes from JSON using typed parsers.');
  buf.writeln('  static $cls parse<$parseTypeParams>(');
  buf.writeln('    Object? json, {');
  for (var i = 0; i < n; i++) {
    buf.writeln('    required ${letters[i]} Function(Object) from${letters[i]},');
  }
  buf.writeln('  }) {');
  for (var i = 0; i < n; i++) {
    buf.writeln('    if (json is ${letters[i]}) return OneOf$n._(json);');
  }
  buf.writeln('    final errors = <(String, Object)>[];');
  for (var i = 0; i < n; i++) {
    buf.writeln('    try {');
    buf.writeln('      return OneOf$n._(from${letters[i]}(json!));');
    // Catch Object, not just Exception: a variant's fromJson discriminates by
    // casting (e.g. `json['x'] as String`), which throws a TypeError — an
    // Error, not an Exception — when the payload is a different variant. An
    // `on Exception` guard lets that escape and aborts the whole parse instead
    // of falling through to the next variant.
    buf.writeln('    } on Object catch (e) {');
    buf.writeln("      errors.add(('\$${letters[i]}', e));");
    buf.writeln('    }');
  }
  buf.writeln('    throw ArgumentError(_oneOfError(json, errors));');
  buf.writeln('  }');
  buf.writeln();

  // value field
  buf.writeln('  /// The wrapped value.');
  buf.writeln('  final Object? value;');
  buf.writeln();

  // toJson
  buf.writeln('  /// Serializes to JSON. Primitives pass through; objects use `toJson()`.');
  buf.writeln('  Object? toJson() {');
  buf.writeln('    final v = value;');
  buf.writeln('    if (v == null || v is String || v is num || v is bool) return v;');
  buf.writeln('    return (v as dynamic).toJson();');
  buf.writeln('  }');
  buf.writeln();

  // ==, hashCode, toString
  buf.writeln('  @override');
  buf.writeln('  bool operator ==(Object other) =>');
  final eqLine = 'identical(this, other) || other is $cls && value == other.value;';
  if (eqLine.length + 6 > 80) {
    buf.writeln('      identical(this, other) ||');
    buf.writeln('      other is $cls && value == other.value;');
  } else {
    buf.writeln('      $eqLine');
  }
  buf.writeln();
  buf.writeln('  @override');
  buf.writeln('  int get hashCode => value.hashCode;');
  buf.writeln();
  buf.writeln('  @override');
  buf.writeln("  String toString() => 'OneOf$n(\$value)';");

  buf.writeln('}');
}
